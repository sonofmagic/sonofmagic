export function resolveIconName(iconHref?: string) {
  if (!iconHref) {
    return ''
  }
  if (iconHref.includes('chorme')) {
    return 'chrome'
  }
  if (iconHref.includes('wechat')) {
    return 'wechat'
  }
  return ''
}

